// fakeFS is a minimal FileSystemView for java cataloger tests. Content is
// stored as raw bytes so binary ZIP fixtures work.
type fakeFS struct {
	entries map[string]*model.FileEntry
	content map[string][]byte
}

func (f *fakeFS) Get(path string) (*model.FileEntry, bool) {
	e, ok := f.entries[path]
	return e, ok
}
func (f *fakeFS) Open(path string) (model.ContentReader, error) {
	c, ok := f.content[path]
	if !ok {
		return nil, errNotFound{}
	}
	return io.NopCloser(bytes.NewReader(c)), nil
}
func (f *fakeFS) Walk(prefix string, fn func(*model.FileEntry) error) error {
	for _, e := range f.entries {
		if e.IsDeleted {
			continue
		}
		if strings.HasPrefix(e.Path, prefix) {
			if err := fn(e); err != nil {
				return err
			}
		}
	}
	return nil
}
func (f *fakeFS) Entries() int { return len(f.entries) }

type errNotFound struct{}

func (errNotFound) Error() string { return "not found" }

// makeZip builds an in-memory ZIP with the given entries (name -> content).
func makeZip(t *testing.T, entries map[string]string) []byte {
	t.Helper()
	buf := &bytes.Buffer{}
	w := zip.NewWriter(buf)
	for name, body := range entries {
		fw, err := w.Create(name)
		if err != nil {
			t.Fatalf("create %s: %v", name, err)
		}
		if _, err := fw.Write([]byte(body)); err != nil {
			t.Fatalf("write %s: %v", name, err)
		}
	}
	if err := w.Close(); err != nil {
		t.Fatalf("close zip: %v", err)
	}
	return buf.Bytes()
}

// makeNestedZip builds an outer ZIP whose entry "lib/inner.jar" is itself a ZIP
// containing the given entries.
func makeNestedZip(t *testing.T, innerName string, innerEntries map[string]string, extra map[string]string) []byte {
	t.Helper()
	inner := makeZip(t, innerEntries)
	merged := make(map[string]string)
	for k, v := range extra {
		merged[k] = v
	}
	merged[innerName] = string(inner)
	return makeZip(t, merged)
}

const pomProps = `#Generated by Maven
#Wed Jan 01 00:00:00 UTC 2024
version=6.0.10
groupId=org.springframework
artifactId=spring-core
`

func TestParseJarWithPomProperties(t *testing.T) {
	jar := makeZip(t, map[string]string{
		"META-INF/maven/org.springframework/spring-core/pom.properties": pomProps,
	})
	fs := &fakeFS{
		entries: map[string]*model.FileEntry{
			"/app/spring-core.jar": {Path: "/app/spring-core.jar", LayerDigest: "sha256:layerJAR"},
		},
		content: map[string][]byte{"/app/spring-core.jar": jar},
	}
	c := New()
	pkgs, err := c.Catalog(context.Background(), fs)
	if err != nil {
		t.Fatalf("Catalog: %v", err)
	}
	if len(pkgs) != 1 {
		t.Fatalf("expected 1 package, got %d", len(pkgs))
	}
	p := pkgs[0]
	if p.Name != "spring-core" {
		t.Errorf("Name = %q", p.Name)
	}
	if p.SourcePackage != "org.springframework" {
		t.Errorf("SourcePackage = %q", p.SourcePackage)
	}
	if p.Version != "6.0.10" {
		t.Errorf("Version = %q", p.Version)
	}
	if p.PURL != "pkg:maven/org.springframework/spring-core@6.0.10" {
		t.Errorf("PURL = %q", p.PURL)
	}
	if p.Type != "java" || p.Ecosystem != "maven" {
		t.Errorf("Type/Ecosystem = %q/%q", p.Type, p.Ecosystem)
	}
	if p.Metadata["source"] != "pom.properties" {
		t.Errorf("source = %q", p.Metadata["source"])
	}
	if p.LayerDigest != "sha256:layerJAR" {
		t.Errorf("LayerDigest = %q", p.LayerDigest)
	}
	if len(p.Locations) != 1 || p.Locations[0].Path != "/app/spring-core.jar" {
		t.Errorf("Locations = %+v", p.Locations)
	}
}

