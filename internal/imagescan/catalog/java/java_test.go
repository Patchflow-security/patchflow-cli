func TestMatch(t *testing.T) {
	c := New()
	// No archives -> false.
	fsEmpty := &fakeFS{entries: map[string]*model.FileEntry{}, content: map[string][]byte{}}
	if c.Match(fsEmpty) {
		t.Fatal("expected Match=false for empty fs")
	}
	// .jar present -> true.
	fsJar := &fakeFS{
		entries: map[string]*model.FileEntry{
			"/x.jar": {Path: "/x.jar"},
		},
		content: map[string][]byte{"/x.jar": makeZip(t, nil)},
	}
	if !c.Match(fsJar) {
		t.Fatal("expected Match=true when .jar exists")
	}
}

