func TestMatch(t *testing.T) {
	c := New()
	if c.Match(&fakeFS{}) {
		t.Fatal("expected Match=false for empty fs")
	}
	fs := newFS("/Cargo.lock", cargoLockV4)
	if !c.Match(fs) {
		t.Fatal("expected Match=true when Cargo.lock present")
	}
}

func TestMatch_MultiplePaths(t *testing.T) {
	c := New()
	fs := &fakeFS{
		entries: map[string]*model.FileEntry{
			"/app/Cargo.lock": {Path: "/app/Cargo.lock", LayerDigest: "sha256:layerAPP"},
		},
		content: map[string]string{"/app/Cargo.lock": cargoLockV4},
	}
	if !c.Match(fs) {
		t.Fatal("expected Match=true for /app/Cargo.lock")
	}
	pkgs, err := c.Catalog(context.Background(), fs)
	if err != nil {
		t.Fatalf("Catalog: %v", err)
	}
	if len(pkgs) != 3 {
		t.Fatalf("expected 3 packages, got %d", len(pkgs))
	}
	if pkgs[0].Locations[0].Path != "/app/Cargo.lock" {
		t.Errorf("location = %q", pkgs[0].Locations[0].Path)
	}
}

func TestParseMalformedCargoLock(t *testing.T) {
	content := `version = 4

[[package]]
name = "good"
version = "1.0.0"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package
name = "bad-no-bracket"
version = "1.0.0"

[[package]]
version = "2.0.0"
source = "registry+https://github.com/rust-lang/crates.io-index"
`
	fs := newFS("/Cargo.lock", content)
	c := New()
	pkgs, err := c.Catalog(context.Background(), fs)
	if err != nil {
		t.Fatalf("Catalog: %v", err)
	}
	// Only "good" should be emitted; the malformed and nameless entries skipped.
	if len(pkgs) != 1 {
		t.Fatalf("expected 1 package, got %d: %+v", len(pkgs), pkgs)
	}
	if pkgs[0].Name != "good" {
		t.Errorf("pkg name = %q, want good", pkgs[0].Name)
	}
}

