func newFS(path, content string) *fakeFS {
	return &fakeFS{
		entries: map[string]*model.FileEntry{
			path: {Path: path, LayerDigest: "sha256:layerRUST"},
		},
		content: map[string]string{path: content},
	}
}

func TestParseCargoLockV4(t *testing.T) {
	fs := newFS("/Cargo.lock", cargoLockV4)
	c := New()
	if !c.Match(fs) {
		t.Fatal("expected Match=true")
	}
	pkgs, err := c.Catalog(context.Background(), fs)
	if err != nil {
		t.Fatalf("Catalog: %v", err)
	}
	if len(pkgs) != 3 {
		t.Fatalf("expected 3 packages (local-crate skipped), got %d", len(pkgs))
	}
	want := []struct{ name, version, purl string }{
		{"Serde", "1.0.171", "pkg:cargo/serde@1.0.171"},
		{"tokio", "1.32.0", "pkg:cargo/tokio@1.32.0"},
		{"rand", "0.8.5", "pkg:cargo/rand@0.8.5"},
	}
	for i, w := range want {
		if pkgs[i].Name != w.name {
			t.Errorf("pkg[%d] name = %q, want %q", i, pkgs[i].Name, w.name)
		}
		if pkgs[i].Version != w.version {
			t.Errorf("pkg[%d] version = %q, want %q", i, pkgs[i].Version, w.version)
		}
		if pkgs[i].PURL != w.purl {
			t.Errorf("pkg[%d] purl = %q, want %q", i, pkgs[i].PURL, w.purl)
		}
		if pkgs[i].Type != "rust" || pkgs[i].Ecosystem != "cargo" {
			t.Errorf("pkg[%d] type/eco = %q/%q", i, pkgs[i].Type, pkgs[i].Ecosystem)
		}
		if pkgs[i].LayerDigest != "sha256:layerRUST" {
			t.Errorf("pkg[%d] layer = %q", i, pkgs[i].LayerDigest)
		}
		if len(pkgs[i].Locations) != 1 || pkgs[i].Locations[0].Path != "/Cargo.lock" {
			t.Errorf("pkg[%d] locations = %+v", i, pkgs[i].Locations)
		}
		if pkgs[i].Metadata["source"] == "" {
			t.Errorf("pkg[%d] missing source metadata", i)
		}
	}
}

